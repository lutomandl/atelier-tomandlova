<h1>O nás</h1>

<p>Lenka Tomandlová vystudovala Libereckou textilní fakultu, oděvní tvorbě se věnuje téměř 30 let. 
	Ateliér sídlí v historickém centru Chebu a je dnes už neoddělitelnou součástí starého města.
</p>

<p class="quote">„Středem mého zájmu i samotnou inspirací je mi přírodní materiál, jeho krása, jedinečnost a specifické vlastnosti, blahodárné pro lidské tělo.”</p>

<p class="quote inverted">„Komfortní oděv pak vzniká promyšleným výběrem tkaniny, individuálně voleným tvarem, hrou barev, kompozicí výtvarného detailu a v neposlední řadě mistrovským krejčovským zpracováním Olgy Dražanové a Nelly Kaiseršotové.“</p>


